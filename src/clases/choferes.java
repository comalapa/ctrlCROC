    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(tblChoferes.getSelectedRow() > -1){
            if(JOptionPane.YES_OPTION == JOptionPane.showConfirmDialog(null,"Esta a punto de eliminar el chofer seleccionado.\n¿Desea continuar?","Alerta",JOptionPane.YES_NO_OPTION)){
                conexion consql = new conexion();

                    Statement stat = consql.conectar();
                    if(stat!=null){
                        if(consql.ejecutarSQL("UPDATE tb_chofer SET estatus_id = 2, fecha_borrado=NOW() WHERE id="+Integer.parseInt(tblChoferes.getValueAt(tblChoferes.getSelectedRow(), 0).toString())+";")){
                            JOptionPane.showMessageDialog(this, "El chofer se elimino correctamente");
                            getDataChofer();
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "Error al intentar aliminer el camion.\nPonsgase en contacto con el administrador del sistema");
                        }
                    }
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

