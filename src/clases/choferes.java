    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if(tblChoferes.getSelectedRow() != -1) {
            conexion consql = new conexion();
            Statement stat = consql.conectar();
            if (stat != null) {
                registroChoferes reg = new registroChoferes(this, true);
                reg.id=Integer.parseInt(tblChoferes.getValueAt(tblChoferes.getSelectedRow(), 0).toString());
                reg.getDatosModificar();
                reg.setVisible(true);
                if(reg.modif){
                    getDataChofer();
                }
                //                 tblCamiones.getValueAt(tblCamiones.getSelectedRow(), 0).toString();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione un camion del catalogo");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

