    private void deleteRowModelTable(){
        // Limpiamos el Modelo de la Tabla
        while (model.getRowCount()>0){
            model.removeRow(0);
        }
    }
    
    private void getDataChofer(){
        getModel();
        deleteRowModelTable();
        conexion consql = new conexion();
        Statement stat = consql.conectar();
        
        if(stat!=null){
           ResultSet result = consql.ejecutarSQLSelect("SELECT a.id,a.codigo,a.nombre,a.apellido_p,a.apellido_m,a.direccion,a.telefono,a.estatus_id,b.nombre AS estatus FROM tb_chofer a, tb_estatus b WHERE a.estatus_id = b.id AND estatus_id = 1;");
            try {
                while(result.next()){
                    model.addRow(new Object[]{result.getInt("id"),result.getString("codigo"),result.getString("nombre"),result.getString("apellido_p"),result.getString("apellido_m"),result.getString("direccion"),result.getString("telefono"),result.getString("estatus")});
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
    
